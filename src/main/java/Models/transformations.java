package Models;

public class transformations {

    private int Init;
    private int Final;
    private int Tool;
    private int Duration;

    public int getInit() {
        return Init;
    }
    public void setInit(int init) {
        Init = init;
    }

    public int getFinal() {
        return Final;
    }
    public void setFinal(int aFinal) {
        Final = aFinal;
    }

    public int getTool() {
        return Tool;
    }
    public void setTool(int tool) {
        Tool = tool;
    }

    public int getDuration() {
        return Duration;
    }
    public void setDuration(int duration) {
        Duration = duration;
    }
}
